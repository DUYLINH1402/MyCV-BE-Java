package com.linhnguyen.portfolio_api.entity;

/**
 * Enum định nghĩa mức độ thành thạo của kỹ năng.
 * Sử dụng 3 mức đơn giản thay vì thang điểm số để dễ hiểu và hiển thị.
 */
public enum SkillLevel {
    /** Mức độ thấp - Đang học hoặc có kiến thức cơ bản */
    LOW,

    /** Mức độ trung bình - Có thể làm việc độc lập */
    MEDIUM,

    /** Mức độ cao - Thành thạo, có thể hướng dẫn người khác */
    HIGH
}

